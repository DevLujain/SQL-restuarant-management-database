CREATE OR REPLACE VIEW VW_STOCK_STATUS AS
SELECT i.INGREDIENTID, i.NAME, i.UNIT, i.STOCK_LEVEL, i.REORDER_LEVEL,
       CASE WHEN i.STOCK_LEVEL <= i.REORDER_LEVEL THEN 'REORDER' ELSE 'OK' END AS STATUS
FROM INGREDIENT i;

CREATE OR REPLACE VIEW VW_ORDER_SUMMARY AS
SELECT o.ORDERID, o.ORDERDATETIME, o.ORDERTYPE, o.TABLENO,
       s.NAME AS STAFF_NAME,
       p.PAYMENTMETHOD, p.PAYMENTSTATUS, p.AMOUNT
FROM ORDERS o
LEFT JOIN STAFF s ON s.STAFFID = o.STAFFID
LEFT JOIN PAYMENT p ON p.PAYMENTID = o.PAYMENTID;


