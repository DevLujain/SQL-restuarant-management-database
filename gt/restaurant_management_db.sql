CREATE OR REPLACE TRIGGER TRG_ORDERLINE_DEDUCT_STOCK
AFTER INSERT ON ORDER_LINE
FOR EACH ROW
DECLARE
  v_neg_cnt NUMBER;
BEGIN
  UPDATE INGREDIENT i
     SET i.STOCK_LEVEL = i.STOCK_LEVEL - (
         SELECT ii.QUANTITY * :NEW.QUANTITY
           FROM INGREDIENT_ITEM ii
          WHERE ii.INGREDIENTID = i.INGREDIENTID
            AND ii.MENUITEMID = :NEW.MENUITEMID
     )
   WHERE EXISTS (
     SELECT 1 FROM INGREDIENT_ITEM ii
      WHERE ii.INGREDIENTID = i.INGREDIENTID
        AND ii.MENUITEMID = :NEW.MENUITEMID
   );

  SELECT COUNT(*) INTO v_neg_cnt
  FROM INGREDIENT WHERE STOCK_LEVEL < 0;

  IF v_neg_cnt > 0 THEN
    RAISE_APPLICATION_ERROR(-20001, 'Insufficient ingredient stock for this order.');
  END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_PURCHASE_ADD_STOCK
AFTER INSERT ON PURCHASEDETAIL
FOR EACH ROW
BEGIN
  UPDATE INGREDIENT
     SET STOCK_LEVEL = STOCK_LEVEL + :NEW.QUANTITY
   WHERE INGREDIENTID = :NEW.INGREDIENTID;
END;
/


