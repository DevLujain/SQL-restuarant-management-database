CREATE OR REPLACE FUNCTION FN_ORDER_TOTAL(p_orderid NUMBER)
RETURN NUMBER IS
  v_total NUMBER := 0;
BEGIN
  SELECT NVL(SUM(QUANTITY * UNITPRICE), 0)
    INTO v_total
    FROM ORDER_LINE
   WHERE ORDERID = p_orderid;
  RETURN v_total;
END;
/

CREATE OR REPLACE PROCEDURE SP_PLACE_ORDER(
  p_ordertype     IN  VARCHAR2,
  p_tableno       IN  NUMBER,
  p_staffid       IN  NUMBER,
  p_paymentmethod IN  VARCHAR2,
  p_paymentstatus IN  VARCHAR2,
  p_orderid       OUT NUMBER
) IS
  v_paymentid NUMBER;
BEGIN
  INSERT INTO PAYMENT(paymentmethod, amount, paymentstatus)
  VALUES (p_paymentmethod, 0, NVL(p_paymentstatus,'PAID'))
  RETURNING paymentid INTO v_paymentid;

  INSERT INTO ORDERS(orderdatetime, ordertype, tableno, staffid, paymentid)
  VALUES (SYSTIMESTAMP, NVL(p_ordertype,'DINE_IN'), p_tableno, p_staffid, v_paymentid)
  RETURNING orderid INTO p_orderid;

  COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE SP_RECEIVE_PO(
  p_supplierid IN  NUMBER,
  p_staffid    IN  NUMBER,
  p_poid       OUT NUMBER
) IS
BEGIN
  INSERT INTO PURCHASEORDER(orderdate, supplierid, staffid)
  VALUES (SYSDATE, p_supplierid, p_staffid)
  RETURNING purchaseorderid INTO p_poid;
  COMMIT;
END;
/


